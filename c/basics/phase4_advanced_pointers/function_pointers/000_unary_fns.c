#include <math.h>

float add(float x, float y) {
  return x + y;
}

float sub(float x, float y) {
  return x - y;
}

float mul(float x, float y) {
  return x * y;
}

double cube(double x) {
  return pow(x, 3);
}

double square(double x) {
  return pow(x, 2);
}

double sine(double x) {
  return sin(x);
}

double exponent(double x) {
  return exp(x);
}
